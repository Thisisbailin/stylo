import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const repoRoot = process.cwd();
const skillsRoot = path.join(repoRoot, ".agents", "skills");
const outputPath = path.join(repoRoot, "agents", "runtime", "skillManifest.generated.ts");

const parseScalar = (raw) => {
  const value = raw.trim();
  if (!value) return "";
  if (
    (value.startsWith('"') && value.endsWith('"')) ||
    (value.startsWith("'") && value.endsWith("'"))
  ) {
    return value.slice(1, -1);
  }
  return value;
};

const parseSimpleYaml = (source) => {
  const result = {};
  let currentListKey = null;
  for (const rawLine of source.split(/\r?\n/)) {
    const line = rawLine.replace(/\t/g, "  ");
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    if (trimmed.startsWith("- ")) {
      if (!currentListKey) {
        throw new Error(`Unexpected list item without key: ${trimmed}`);
      }
      result[currentListKey].push(parseScalar(trimmed.slice(2)));
      continue;
    }
    currentListKey = null;
    const match = line.match(/^([A-Za-z0-9_]+):(?:\s*(.*))?$/);
    if (!match) {
      throw new Error(`Unsupported YAML line: ${line}`);
    }
    const [, key, rawValue = ""] = match;
    if (!rawValue.trim()) {
      result[key] = [];
      currentListKey = key;
      continue;
    }
    result[key] = parseScalar(rawValue);
  }
  return result;
};

const escapeText = (value) =>
  JSON.stringify(value ?? "");

const buildFile = (entries) => {
  const registryEntries = entries
    .map((entry) => {
      const tags = Array.isArray(entry.tags) ? entry.tags : [];
      const preferredTools = Array.isArray(entry.preferredTools) ? entry.preferredTools : [];
      const disabledTools = Array.isArray(entry.disabledTools) ? entry.disabledTools : [];
      const implicitInvocationHints = Array.isArray(entry.implicitInvocationHints) ? entry.implicitInvocationHints : [];
      return `  ${JSON.stringify(entry.id)}: {
    manifest: {
      id: ${escapeText(entry.id)},
      title: ${escapeText(entry.title)},
      description: ${escapeText(entry.description)},
      sourcePath: ${escapeText(entry.sourcePath)},
      activationMode: ${escapeText(entry.activationMode || "explicit")} as const,
      tags: ${JSON.stringify(tags)},
      preferredTools: ${JSON.stringify(preferredTools)},
      disabledTools: ${JSON.stringify(disabledTools)},
      implicitInvocationHints: ${JSON.stringify(implicitInvocationHints)},
    },
    resolve: async () => {
      const mod = await import(${JSON.stringify(entry.entryModule)});
      return mod[${JSON.stringify(entry.resolverExport)}](SKILL_REGISTRY[${JSON.stringify(entry.id)}].manifest);
    },
  }`;
    })
    .join(",\n");

  return `/* eslint-disable */
// Generated by scripts/generate-skill-manifest.mjs
import type { QalamResolvedSkill, QalamSkillManifest } from "./types";

type SkillRegistryEntry = {
  manifest: QalamSkillManifest;
  resolve: () => Promise<QalamResolvedSkill>;
};

const SKILL_REGISTRY: Record<string, SkillRegistryEntry> = {
${registryEntries}
};

export const GENERATED_SKILL_MANIFESTS: QalamSkillManifest[] = Object.values(SKILL_REGISTRY).map((entry) => entry.manifest);

export const resolveGeneratedSkill = async (id: string): Promise<QalamResolvedSkill | null> => {
  const entry = SKILL_REGISTRY[id];
  if (!entry) return null;
  return entry.resolve();
};
`;
};

const loadSkillEntries = async () => {
  const dirs = await readdir(skillsRoot, { withFileTypes: true });
  const entries = [];
  for (const dir of dirs) {
    if (!dir.isDirectory()) continue;
    const metadataPath = path.join(skillsRoot, dir.name, "agents", "openai.yaml");
    try {
      const source = await readFile(metadataPath, "utf8");
      const parsed = parseSimpleYaml(source);
      for (const field of ["id", "title", "description", "sourcePath", "entryModule", "resolverExport"]) {
        if (!parsed[field]) throw new Error(`Missing required field "${field}" in ${metadataPath}`);
      }
      entries.push(parsed);
    } catch (error) {
      if (error && error.code === "ENOENT") continue;
      throw error;
    }
  }
  return entries.sort((a, b) => String(a.id).localeCompare(String(b.id)));
};

const main = async () => {
  const entries = await loadSkillEntries();
  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(outputPath, buildFile(entries), "utf8");
  console.log(`Generated ${path.relative(repoRoot, outputPath)} with ${entries.length} skill(s).`);
};

main().catch((error) => {
  console.error("Failed to generate skill manifest");
  console.error(error);
  process.exitCode = 1;
});
